    //Save Image
    public void SaveImage(GraphApp frame) {
        
        if(frame!=null)
        {
            
            java.io.File ff=FileIO.getImageFile(frame);
        
            //获得画板的长和宽
            Dimension imageSize = frame.drawPanel1.getSize();  
        
            BufferedImage image = new BufferedImage(imageSize.width,imageSize.height, BufferedImage.TYPE_INT_RGB);
        
            //Graphics2D createGraphics() 创建一个 Graphics2D，可以将它绘制到此 BufferedImage 中。 
            Graphics2D g2D = image.createGraphics();  
        
            frame.drawPanel1.paint(g2D);  
        
            g2D.dispose();  
        
            try {
                String ntype=FileIO.getFileExt(ff);
                javax.imageio.ImageIO.write(image,ntype,ff);  
            } 
            catch (java.io.IOException e) { 
                e.printStackTrace();
                return;
            }  
        }//end if
        
    }
    
    
    public void LoadImage(GraphApp frame) {
        
        java.io.File file=FileIO.getImageFile(frame);
        Image image=null;
        try {
            image=javax.imageio.ImageIO.read(file);  
        }
        catch (java.io.IOException e) {
            e.printStackTrace();return;
        }  
        
        frame.drawPanel1.setImage(image);
        frame.repaint();
        
    }
    
    


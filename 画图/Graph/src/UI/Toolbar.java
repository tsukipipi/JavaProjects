    //delete graph
    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        
        //delete graph
        int i=jComboBox1.getSelectedIndex();
        if(i!=-1)Toolbar.graph.remove(i);
        if(Toolbar.graph.size()==0)
            jComboBox1.setSelectedIndex(-1);
        else if(i<Toolbar.graph.size())
            jComboBox1.setSelectedIndex(i);
        getParent().repaint();
    }//GEN-LAST:event_jButton2ActionPerformed

    //edit graph
    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        
        //获得要修改的图形的索引
        int i=jComboBox1.getSelectedIndex();
        if(i!=-1){
            
            MyShape s=graph.get(i);
            //edit Circle
            if(s instanceof Circle)
            {
                Circle c=(Circle)CircleInputDialog.getCircle((Frame)(getRootPane().getParent()),(Circle)s);
                if(c!=null)
                {
                    ((Circle)s).setX(c.getX());
                    ((Circle)s).setY(c.getY());
                    ((Circle)s).setR(c.getR());
                }
            }
            //edit Rectangle
            else if(s instanceof Graph.Rectangle)
            {
                Graph.Rectangle r=(Graph.Rectangle)RectangleInputDialog.getRectangle((Frame)(getRootPane().getParent()),(Graph.Rectangle)s);
                if(r!=null)
                {
                    ((Graph.Rectangle)s).setX(r.getX());
                    ((Graph.Rectangle)s).setY(r.getY());
                    ((Graph.Rectangle)s).setLength(r.getLength());
                    ((Graph.Rectangle)s).setWidth(r.getWidth());
                }
            }
            //edit Triangle
            else if(s instanceof Triangle)
            {
                Triangle t=(Triangle)TriangleInputDialog.getTriangle((Frame)(getRootPane().getParent()),(Triangle)s);
                if(t!=null)
                {
                    ((Triangle)s).setX0(t.getX0());
                    ((Triangle)s).setY0(t.getY0());
                    ((Triangle)s).setX1(t.getX1());
                    ((Triangle)s).setY1(t.getY1());
                    ((Triangle)s).setX2(t.getX2());
                    ((Triangle)s).setY2(t.getY2());
                }
            }
            //edit Square
            else if(s instanceof Square)
            {
                Square sq=(Square)SquareInputDialog.getSquare((Frame)(getRootPane().getParent()),(Square)s);
                if(sq!=null)
                {
                    ((Square)s).setX(sq.getX());
                    ((Square)s).setY(sq.getY());
                    ((Square)s).setSide(sq.getSide());
                }
            }
            //edit Pentagon
            else if(s instanceof Pentagon)
            {
                Pentagon p=(Pentagon)PentagonInputDialog.getPentagon((Frame)(getRootPane().getParent()),(Pentagon)s);
                if(p!=null)
                {
                    ((Pentagon)s).setX0(p.getX0());
                    ((Pentagon)s).setY0(p.getY0());
                    ((Pentagon)s).setX1(p.getX1());
                    ((Pentagon)s).setY1(p.getY1());
                    ((Pentagon)s).setX2(p.getX2());
                    ((Pentagon)s).setY2(p.getY2());
                    ((Pentagon)s).setX3(p.getX3());
                    ((Pentagon)s).setY3(p.getY3());
                    ((Pentagon)s).setX4(p.getX4());
                    ((Pentagon)s).setY4(p.getY4());
                }
            }
            //edit Line
            else if(s instanceof Line)
            {
                Line l=(Line)LineInputDialog.getLine((Frame)(getRootPane().getParent()),(Line)s);
                if(l!=null)
                {
                    ((Line)s).setX1(l.getX1());
                    ((Line)s).setY1(l.getY1());
                    ((Line)s).setX2(l.getX2());
                    ((Line)s).setY2(l.getY2());
                }
            }
            //edit Text
            else if(s instanceof Text)
            {
                Text t=(Text)TextInputDialog.getText((Frame)(getRootPane().getParent()),(Text)s);
                if(t!=null)
                {
                    ((Text)s).setText(t.getText());
                }
            }
        }
        getParent().repaint();
    }//GEN-LAST:event_jButton3ActionPerformed

